package br.com.projetocosnnectcorrecao;

/**
 *
 * @author MatheusA
 */
public class GestoraDeEvento {
    
    private String nomeGestora;
    private String cnpj;
    private String emailGestora;
    private String senhaGestora;

    public String getNomeGestora() {
        return nomeGestora;
    }

    public void setNomeGestora(String nomeGestora) {
        this.nomeGestora = nomeGestora;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getEmailGestora() {
        return emailGestora;
    }

    public void setEmailGestora(String emailGestora) {
        this.emailGestora = emailGestora;
    }

    public String getSenhaGestora() {
        return senhaGestora;
    }

    public void setSenhaGestora(String senhaGestora) {
        this.senhaGestora = senhaGestora;
    }
    
    
}
